//Описание приложения:
//Приложение имитирует работу банка.
//Возможо создать нового клиента. Ему необходимо указать ФИО и возраст. Клиент должен быть не моложе 18 лет.
//Так же реализована возможность выполнения ряда операций над клиентом (получить объект клиента по Id,
//удалить клиента, получить список счетов клиента и список кредитов клиента). При удалении клиента проверятеся наличие
