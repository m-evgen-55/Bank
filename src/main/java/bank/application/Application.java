//все связанные счета и кредиты.
//Клиенту можно открыть счет  и выполнить над счетом ряд операций (положить деньги на счет, снять деньши со счета,
//получить объект счета по Id, получить Id клиента, к которому привязан счет, по Id счета, удлить счет).
//Клиент может сделать заявку на кредит. Есть небольшая логика ободрения кредита (см CreditServiseImpl).
//Реализована возможность внести платеж по кредиту, а так же по Id кредита получить Id клиента, к которому привязан
//данный кредит.
